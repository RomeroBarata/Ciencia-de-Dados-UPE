  group_by(country) %>% 
  summarise(r = abs(cor(lifeExp, gdpPercap))) %>% 
  arrange(desc(r)) %>% 
  slice(1:10)

## Exercício 07 ----------------------------------------------------------------
# Que combinações de continente e ano (excluindo a Ásia) possuem a maior 
# população média considerando todos os seus países? Exiba os resultados em 
